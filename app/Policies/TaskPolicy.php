<?php

namespace App\Policies;

use App\Models\Task;
use App\Models\User;
use Illuminate\Auth\Access\HandlesAuthorization;

class TaskPolicy
{
    use HandlesAuthorization;

    /**
     * Determine whether the user can view any models.
     *
     * @param  \App\Models\User  $user
     * @return mixed
     */
    public function viewAny(User $user)
    {
        // ici tout le monde peu voir les tâches
        return true;
    }

    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Task  $task
     * @return mixed
     */
    public function view(User $user, Task $task)
    {
        // ici tout le monde peu voir le détail d'une tache
        return true;
    }

    /**
     * Determine whether the user can create models.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Board  $board
     * @return mixed
     */
    public function create(User $user, Board $board)
    {
        // Ici seul les utilisateur du board et le propriétaire peuvent créer une tache
        return (Auth::user() == $user && $board->users->find($user->id) != null);
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Task  $task
     * @return mixed
     */
    public function update(User $user, Task $task)
    {
        // Ici on a la fonction qui permettra au créateur de la tache ainsi que les utilisateur assignés au board de modifier la tache auquel ils appartiennents 
        return ($task->board->users->find($user->id) !=null || $task->participants->find($user->id) != null);
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Task  $task
     * @return mixed
     */
    public function updateTodo(User $user, Task $task)
    {
        // ici on permet au utilisateur de la tache de changer le status de la tache
        return ($task->assignedUsers->find($user->id) != null);
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Task  $task
     * @return mixed
     */
    public function updateParticipant(User $user, Task $task)
    {
        return ($task->board->users->find($user->id) != null);
    }

    /**
     * Determine whether the user can delete the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Task  $task
     * @return mixed
     */
    public function delete(User $user, Task $task)
    {
        ($task->board->owner == $user || $task->participants->find($user->id) != null);
    }

